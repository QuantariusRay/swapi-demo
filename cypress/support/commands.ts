Cypress.Commands.add('searchFlow', () => {
  cy.intercept(
    'GET',
    `www.swapi.tech/api/people/?name=Luke%20Skywalker`,
    { fixture: 'luke-search.json' }
  ).as('luke');

  cy.get('input')
    .type('Luke Skywalker');

  cy.get('button')
    .contains('Search')
    .click();
})
